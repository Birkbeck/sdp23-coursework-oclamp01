package sml.instruction;

import org.junit.jupiter.api.AfterEach;
import org.junit.jupiter.api.Assertions;
import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;

import sml.Instruction;
import sml.Machine;
import sml.Registers;

import static sml.Registers.Register.*;

class MulInstructionTest {
    private Machine machine;
    private Registers registers;

    @BeforeEach
    void setUp() {
        machine = new Machine(new Registers());
        registers = machine.getRegisters();
        //...
    }

    @AfterEach
    void tearDown() {
        machine = null;
        registers = null;
    }

    @Test
    void executeValid() {
        registers.set(EAX, 5);
        registers.set(EBX, 6);
        Instruction instruction = new MulInstruction(null, EAX, EBX);
        instruction.execute(machine);
        Assertions.assertEquals(30, machine.getRegisters().get(EAX));
    }

    @Test
    void executeValidTwo() {
        registers.set(EAX, -5);
        registers.set(EBX, 6);
        Instruction instruction = new MulInstruction(null, EAX, EBX);
        instruction.execute(machine);
        Assertions.assertEquals(-30, machine.getRegisters().get(EAX));
    }

    @Test
    void testToStringNoLabel() {
        registers.set(EAX, 3);
        registers.set(EDX, 4);
        Instruction instruction = new MulInstruction(null, EAX, EDX);
        Assertions.assertEquals("mul EAX EDX", instruction.toString());
    }

    @Test
    void testToStringWithLabel() {
        registers.set(EAX, 3);
        registers.set(EDX, 4);
        Instruction instruction = new MulInstruction("a8", EAX, EDX);
        Assertions.assertEquals("a8: mul EAX EDX", instruction.toString());
    }
}